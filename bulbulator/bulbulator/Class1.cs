using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace bulbulator
{
    internal class bulbulator
    {
        public int dodawanie(int n, int k)
        {
            return n + k;
        }
        public int odejmowanie(int n, int k)
        {
            return n - k;
        }
        public int mnozenie(int n, int k)
        {
            return n * k;
        }
        public int dzielenie(int n, int k)
        {
            return n / k;
        }
    }
}
